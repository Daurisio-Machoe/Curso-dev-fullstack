
/* Operadores Artimeticos

Adição (+)
Subitração (-)
Multiplicação (*)
Divisão (/)

** Atributos
Incremento (++)
Decremento (--)

*/

// Adição (+)
let a = 10
let b = 5
let c = a + b
console.log(c)

// Subitração (-)
let d = 20
let e = 16
let f = e - d
console.log(f)

// Multiplicação (*)
let g = d * b
console.log(g)

// Divisão (/)
let h = g / a
console.log(h)

// Incremento (++)
h += 3
console.log(h)

// Decremento (--)

g -= 3
console.log(g)